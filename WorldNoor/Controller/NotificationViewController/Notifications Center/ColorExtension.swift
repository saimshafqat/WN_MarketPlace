import Foundation
import UIKit

extension UIColor {
    
    class var notificationUnReadColor: UIColor {
        return UIColor(red: 231.0 / 255.0, green: 242.0 / 255.0, blue: 254.0 / 255.0, alpha: 1.0)
    }
    
    class var notificationReadColor: UIColor {
        return .white
    }
    
    class var notificationTabsSelectedTextColor: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 150.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
}
